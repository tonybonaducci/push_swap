#include <stdio.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*c;
	size_t	i;

	i = 0;
	c = s;
	while (n > i)
	{
		c[i] = '\0';
		i++;
	}
}
